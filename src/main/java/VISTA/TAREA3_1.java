     private void calcularVenta() {
        try {
            double precioPorDocena = Double.parseDouble(txtprecio.getText());
            int cantidadDocenas = Integer.parseInt(txtcantidad.getText());

            Producto producto = new Producto(precioPorDocena, cantidadDocenas);
            double importeCompra = producto.calcularImporteCompra();
            double descuento = producto.calcularDescuento();
            double importeAPagar = producto.calcularImporteAPagar();
            int lapiceros = producto.calcularLapiceros();

            // Mostrar resultados en la interfaz
            txtResultado.setText(""); // Limpiar el área de texto
            txtResultado.append("\tRESULTADOS\n");
            txtResultado.append("\nImporte de la compra: S/ " + String.format("%.2f", importeCompra));
            txtResultado.append("\nDescuento aplicado: S/ " + String.format("%.2f", descuento));
            txtResultado.append("\nImporte a pagar: S/ " + String.format("%.2f", importeAPagar));
            txtResultado.append("\nLapiceros de obsequio: " + lapiceros);
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese valores numéricos válidos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

